package ch04.unit03.forEX;

public class EX05 {

	public static void main(String[] args) {
		int n;
		
		// 짝수
		for(n=2;n<=10;n+=2)
		{
			System.out.println(n + " ");
		}
		System.out.println("밖 : " + n);
		
		//홀수
		for(n=1;n<=10;n+=2)
		{
			System.out.println(n + " ");
		}
		System.out.println("밖 : " + n);

	}

}
